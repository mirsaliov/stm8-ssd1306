#ifndef _OLED_SSD1306_H_
#define _OLED_SSD1306_H_

/*
 * Инициализация OLED-дисплея SSD1306.
 * Настраивает режим адресации, контраст,
 * включает встроенный charge pump.
 */
void oled_init(void);

/*
 * Полная очистка экрана.
 * Все пиксели переводятся в состояние «выключено».
 */
void oled_clear(void);

/*
 * Установка позиции курсора.
 *
 * page — номер страницы (0–7), каждая по 8 пикселей по вертикали
 * col  — номер столбца (0–127)
 */
void oled_setpos(unsigned char page, unsigned char col);

/*
 * Вывод одного ASCII-символа.
 * Поддерживается автоматический перенос строки.
 */
void oled_putc(char c);

/*
 * Вывод строки символов.
 * Поддерживает '\n' — перевод строки.
 */
void oled_puts(char *s);

#endif
